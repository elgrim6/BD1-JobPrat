package Objectos;

public class Viagem_Duracao
{
	private int cod_viagem,duracao;

	public Viagem_Duracao(int cod_viagem, int duracao) {
		super();
		this.cod_viagem = cod_viagem;
		this.duracao = duracao;
	}

	public int getCod_viagem() {
		return cod_viagem;
	}

	public int getDuracao() {
		return duracao;
	}
}
